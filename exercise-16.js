function graduates (students) {
  // Code disini
  let graduate = {};
  for(let i in students){
    if(graduate[students[i].class] === undefined && students[i].score > 75){
      graduate[students[i].class] = [];
    }
    if(students[i].score > 75){
      let obj = {};
      obj.name = students[i].name;
      obj.score = students[i].score;
      graduate[students[i].class].push(obj);
    }
  }

  return graduate;
}

